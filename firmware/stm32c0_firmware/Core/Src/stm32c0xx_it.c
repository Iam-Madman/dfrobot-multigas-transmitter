/**
  * @brief This function handles USART1 interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
  if (LL_USART_IsActiveFlag_RXNE(USART1)) {
    uint8_t data = LL_USART_ReceiveData8(USART1);
    
    // Check for frame start marker (0xFF)
    if (gas_rx_index == 0 && data != 0xFF) {
      return; 
    }
    
    if (gas_rx_index < GAS_RX_BUF_SIZE) {
      gas_rx_buffer[gas_rx_index++] = data;
      
      // Fixed 9-byte packet frame limit
      if (gas_rx_index >= 9) { 
        gas_data_ready = 1; 
      }
    } else {
      gas_rx_index = 0;
    }
  }
  /* USER CODE END USART1_IRQn 0 */
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

/**
  * @brief This function handles USART2 interrupt.
  */
void USART2_IRQHandler(void)
{
  /* USER CODE BEGIN USART2_IRQn 0 */
  if (LL_USART_IsActiveFlag_RXNE(USART2)) {
    uint8_t byte = LL_USART_ReceiveData8(USART2);
    if (modbus_rx_index < MODBUS_BUF_SIZE && !modbus_frame_complete) {
      modbus_rx_buffer[modbus_rx_index++] = byte;
      modbus_last_rx_tick = uwTick; // Refresh silent period timer
    }
  }
  /* USER CODE END USART2_IRQn 0 */
  /* USER CODE BEGIN USART2_IRQn 1 */

  /* USER CODE END USART2_IRQn 1 */
}

